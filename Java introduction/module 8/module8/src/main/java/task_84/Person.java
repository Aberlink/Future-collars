package task_84;

public class Person {

    String name;
    String surname;
    Integer birthYear;
    Integer height;
    Double weight;

    public Person(String name, String surname, int birthYear, int height, double weight) {
        this.name = name;
        this.surname = surname;
        this.birthYear = birthYear;
        this.height = height;
        this.weight = weight;
    }
}
